package com.example.anton.dvachmobile2.Json;

import java.io.Serializable;

/**
 * Created by Anton on 22.03.2016.
 */
public class Files implements Serializable {
    String height;
    String md5;
    String name;
    String path;
    String size;
    String thumbnail;
    String tn_height;
    String tn_width;
    String type;
    String width;

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getMd5() {
        return md5;
    }

    public void setMd5(String md5) {
        this.md5 = md5;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getThumbnail() {
        return thumbnail;
    }

    public void setThumbnail(String thumbnail) {
        this.thumbnail = thumbnail;
    }

    public String getTn_height() {
        return tn_height;
    }

    public void setTn_height(String tn_height) {
        this.tn_height = tn_height;
    }

    public String getTn_width() {
        return tn_width;
    }

    public void setTn_width(String tn_width) {
        this.tn_width = tn_width;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getWidth() {
        return width;
    }

    public void setWidth(String width) {
        this.width = width;
    }
}
